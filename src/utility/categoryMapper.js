function getCodesByNames(names, map) {
  const codes = names.map((name) => {
    for (const [code, fullName] of map.entries()) {
      if (fullName === name) {
        return code;
      }
    }
    return null; // Return null for unknown names
  });
  return codes;
}
export function categoryMapper(
  selectedCategories,
  selectedLanguages,
  selectedCountries
) {
  const categoriesLowercase = selectedCategories.map((element) =>
    element.toLowerCase()
  );
  const languageCodes = getCodesByNames(selectedLanguages, languageMap);
  const countryCodes = getCodesByNames(selectedCountries, countryMap);

  return {
    categoriesLowercase,
    languageCodes,
    countryCodes,
  };
}
